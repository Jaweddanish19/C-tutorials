// Relational operators
/*
== (a==b) it shows relation
(a=b) it tells to put the value of b to a
*/

#include <stdio.h>
#include <math.h>

int main (){
    printf("%d\n",4==4);
    printf("%d\n",4>3);
    printf("%d\n",4<3);
    
    //if we assign an equal to operator with less than or greater than operator then compiler will check both the operations equal to as well as greater or less than operators
    printf("%d\n",4<=3);
    printf("%d\n",4>=3);
    printf("%d\n",4!=3);
    

    return 0;

}
